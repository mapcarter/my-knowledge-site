chars = set(UI)
chars.update(COMMON)
for p in DOCS.rglob("*.md"):
    try:
        chars.update(p.read_text(encoding="utf-8"))
    except Exception:
        pass

# 思源宋体子集：全部字符
pathlib.Path(ROOT / "glyphs.txt").write_text("".join(sorted(chars)), encoding="utf-8")

# JetBrains Mono 子集：仅拉丁/符号（代码用）
mono = set()
for c in chars:
    o = ord(c)
    if (
        o < 0x2000
        or (0x2000 <= o <= 0x206F)        # 通用标点
        or (0x2190 <= o <= 0x21FF)        # 箭头
        or (0x2200 <= o <= 0x22FF)        # 数学运算符
        or (0x2500 <= o <= 0x257F)        # 制表/画线
        or (0x3000 <= o <= 0x303F)        # CJK 符号与标点
    ):
        mono.add(c)
pathlib.Path(ROOT / "glyphs_mono.txt").write_text("".join(sorted(mono)), encoding="utf-8")

print(f"glyphs.txt: {len(chars)} 个字符")
